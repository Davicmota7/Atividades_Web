
let numA = 90, numB = 2;
let resultadoSoma = numA + numB;
console.log(`Resultado da adição entre ${numA} e ${numB} é ${resultadoSoma}`);

let resultadoSubtracao = numA - numB;
console.log(`Resultado da subtração entre ${numA} e ${numB} é ${resultadoSubtracao}`);

let resultadoMultiplicacao = numA * numB;
console.log(`Resultado da multiplicação entre ${numA} e ${numB} é ${resultadoMultiplicacao}`);

let resultadoDivisao = numA / numB;
console.log(`Resultado da divisão entre ${numA} e ${numB} é ${resultadoDivisao}`);

let resultadoModulo = numA % numB;
console.log(`Resultado do módulo entre ${numA} e ${numB} é ${resultadoModulo}`);

let resultadoExponenciacao = numA ** numB;
console.log(`Resultado da exponenciação entre ${numA} e ${numB} é ${resultadoExponenciacao}`);